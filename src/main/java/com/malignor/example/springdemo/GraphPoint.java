package com.malignor.example.springdemo;

public class GraphPoint {
    private float x;
    private float y;

    public GraphPoint(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public void setX(float x) {
        this.x = x;
    }

    public void setY(float y) {
        this.y = y;
    }

}
